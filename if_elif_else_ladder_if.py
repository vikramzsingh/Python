#WAP to Show week day if_elif_elif_else(ladder if)
n=int(input("Enter the day: "))
if(n==1):
    print("Mon")
elif(n==2):
    print("Tue")
elif(n==3):
    print("Wed")
elif(n==4):
    print("Thr")
elif(n==5):
    print("Fri")
elif(n==6):
    print("Sat")
elif(n==7):
    print("Sun")
else:
    print("Invalid")
